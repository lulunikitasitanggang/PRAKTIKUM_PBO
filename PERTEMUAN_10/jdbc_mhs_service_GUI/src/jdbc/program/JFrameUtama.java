    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String idText = jTextField2.getText().trim();
        String nama = jTextField4.getText().trim();
        
        if (idText.isEmpty() || nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "ID dan Nama harus diisi!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
            }
        
        try {
            int id = Integer.parseInt(idText);
            
            // Get existing data
            mahasiswa mhs = service.getById(id);
            if (mhs == null) {
                JOptionPane.showMessageDialog(this, 
                    "Data dengan ID " + id + " tidak ditemukan!", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Update data
            mhs.setNama(nama);
            service.update(mhs);
            
            // Refresh lists
            idListModel.clear();
            namaListModel.clear();
            List<mahasiswa> listMahasiswa = service.getAll();
            for (mahasiswa m : listMahasiswa) {
                idListModel.addElement(String.valueOf(m.getId()));
                namaListModel.addElement(m.getNama());
            }
            
            // Clear inputs
            jTextField2.setText("");
            jTextField4.setText("");
            
            JOptionPane.showMessageDialog(this, 
                "Data berhasil diupdate!", 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "ID harus berupa angka!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String idText = jTextField3.getText().trim();
        
        if (idText.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "ID harus diisi!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            int id = Integer.parseInt(idText);
            
            // Confirm deletion
            int confirm = JOptionPane.showConfirmDialog(this, 
                "Yakin ingin menghapus data dengan ID " + id + "?", 
                "Konfirmasi Hapus", 
                JOptionPane.YES_NO_OPTION);
            
            if (confirm == JOptionPane.YES_OPTION) {
                service.delete(id);
                
                // Refresh lists
                idListModel.clear();
                namaListModel.clear();
                List<mahasiswa> listMahasiswa = service.getAll();
                for (mahasiswa m : listMahasiswa) {
                    idListModel.addElement(String.valueOf(m.getId()));
                    namaListModel.addElement(m.getNama());
                }
                
                // Clear inputs
                jTextField3.setText("");
                jTextField5.setText("");
                
                JOptionPane.showMessageDialog(this, 
                    "Data berhasil dihapus!", 
                    "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "ID harus berupa angka!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

